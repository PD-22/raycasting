let players = {};
let items = {};
io.sockets.on('connection', (socket) => {
    let { id } = socket;

    console.log('joined:', id);

    socket.emit('start', { myId: id, worldMap, items });

    players[id] = {};
    logPlayers();

    socket.on('update player', data => {
        let { id, player } = data;
        players[id] = player;
        socket.broadcast.emit('update player', { id, player });
    })

    socket.on('toggle door', door => {
        socket.broadcast.emit('toggle door', door);
    })

    socket.on('disconnect', () => {
        console.log('left:', id);

        delete players[id];
        io.emit('delete player', id);
        logPlayers();
    });
});

function logPlayers() {
    let ids = Object.keys(players);
    console.log(`players[${ids.length}]`, ids);
}

